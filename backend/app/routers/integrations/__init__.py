"""
Пакет inbound-интеграций — приёмники вебхуков от внешних систем.

Архитектура (зеркало outbox-воркера):
  • `event_outbox_worker.py` → outbound: CRM шлёт события наружу;
  • `routers/integrations/*` → inbound: внешние системы шлют события в CRM.

Каждый файл `<vendor>_inbound.py` — отдельный приёмник.  Каждый делает
четыре вещи в одинаковом порядке:

  1. Верифицирует подпись (HMAC SHA-256 или вендорский метод).
  2. Идемпотентно парсит payload и проверяет, не обрабатывали ли уже
     (дедуп по `source_type`+`source_id`).
  3. Делает CRM-side мутацию (создаёт документ/задачу/etc.).
  4. Эмитит event в outbox для внутренних слушателей (`*.after_inbound`),
     чтобы поиск / BI / Telegram-нотификации тоже могли отреагировать.

Конвенции маршрутизации:
  • prefix: `/api/v1/integrations/<vendor>`
  • auth: НЕТ обычной cookie-аутентификации; вместо этого HMAC + IP allow-list
    (последний — на уровне nginx/cloudflare). Все интеграционные пути ДОЛЖНЫ
    быть в `open_prefixes` AuthMiddleware (`main.py`).
  • Ответ: 2xx — обработали (или дедуплицировали), 4xx — клиент виноват
    (плохая подпись, плохой payload), 5xx — наш баг.
    Идемпотентность: повторный POST с тем же external_id возвращает 200 OK
    как первый — без побочных эффектов.

Планируемые приёмники (для июльского запуска):
  • `diadoc_inbound`   — статусы ЭДО от Контур.Диадок (PILOT, см. этот файл);
  • `telegram_inbound` — webhook бота (входящие сообщения как leads);
  • `bank_inbound`     — выписки от Точки/Альфы/Райффа (формат 1С 1.03);
  • `gosklyuch_inbound`— Госключ ЭЦП callback;
  • `sod_inbound`      — Sarex/ProjectPoint/Signal (по мере подключения);
  • `1c_inbound`       — двусторонняя синхронизация с Бухгалтерией 8.3.

Шаблон — `diadoc_inbound.py`.  Все остальные строятся по той же схеме:
четыре шага в одной транзакции, fail-closed на подписи, idempotent.
"""
